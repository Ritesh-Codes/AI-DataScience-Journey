# Declaring variables of different types
name = "Ritesh" # int
age = "26" # float
height = "5.10" # string
is_civil_engineer = True # boolean

# Printing tem one by one
print("Ritesh", name)
print("26", age)
print("5.10", height)
print("True", is_civil_engineer)

#checking their types
print(type(name)) # <class 'str'>
print(type(age)) # <class 'int'>
print(type(height)) # <class 'float'>
print(type(is_civil_engineer)) # <class 'bool'>