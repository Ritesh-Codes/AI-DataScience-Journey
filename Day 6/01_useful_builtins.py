# Demonstrate len(), type(), int(), float(), str(), input()

# len()
print("Length of 'hello': ", len("hello"))

# type()
print("Type of 42: ", type(42))
print("Type of 3.14: ", type(3.14))
print("Type of 'Python': ", type("Python"))

# input()
user_name = input("Enter your name: ")
print("You entered: ", user_name)

# int(), float(), str()
print("int('10'): ", int("10"))
print("float('3.14'): ", float("3.14"))
print("str(100): ", str(100))